package inheritance;

public class Parent {

	public static void main(String[] args) {

	}

	public void car() {
		System.out.println("i am parent car method(red Car)");
	}

	protected void house() {
		System.out.println("i am parent house method");
	}

	void tv() {
		System.out.println("i am parent tv method");

	}

	private void cash() {
		System.out.println("i am parent cash method");

	}

	final void gold() {
		System.out.println("i am parent gold method");

	}

}
